    private void settingsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_settingsActionPerformed
        userSettings usb = new userSettings();
        SessionClass sess = SessionClass.getInstance();
        int user_id = sess.getUid();
                
        usb.id.setText(""+sess.getUid());
        usb.fn.setText(""+sess.getFname());
        usb.ln.setText(""+sess.getLname());
        usb.usn.setText(""+sess.getUsn());
        usb.el.setText(""+sess.getEmail());
        usb.type.setSelectedItem(""+sess.getType());
        usb.us.setSelectedItem(""+sess.getStatus());
        usb.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_settingsActionPerformed

